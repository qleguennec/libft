#include <libft.h>

char		*ft_itoa(int n)
{
	char	s[12];
	int		i;
	int		neg;
	int		min;

	i = 0;
	neg = (n < 0);
	min = (n == -2147483648);
	if (!n)
		s[i++] = '0';
	if (min)
		n++;
	if (neg)
		n = -1 * n;
	while (n != 0)
	{
		s[i++] = (n % 10) + 48;
		n /= 10;
	}
	if (min)
		*s = '8';
	if (neg)
		s[i++] = '-';
	s[i] = '\0';
	return (ft_strrev(s));
}
